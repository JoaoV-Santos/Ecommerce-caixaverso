package model;

public class Pedido {
    private String nome;
    private double valorCadastrado;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getValorCadastrado() {
        return valorCadastrado;
    }

    public void setValorCadastrado(double valorCadastrado) {
        this.valorCadastrado = valorCadastrado;
    }
}
